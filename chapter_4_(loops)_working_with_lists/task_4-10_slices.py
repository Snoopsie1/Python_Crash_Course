# Make a list and add several lines that do the following:
cryptids = ["mothman", "chessie", "loveland frogman", "big foot", "snipe", "chupacabra", "dingbelle", "quetzo", "grim reaper"]
# - Print the message "The first three items in the list are:"
#   Then use a slice to print the first three items from said list.
print("The first three items in the list are:")
print(cryptids[:3])
# - Print the message "Three items from the middle of the list are:"
#   Use a slice to print three items from the middle of the list.
print("Three items from the middle of the list are:")
print(cryptids[3:6])
# - Print the message "The last three items in the list are:"
#   Use a slice to print the last three items in the list.
print("The last three items in the list are:")
print(cryptids[6:])
