#You can also just add new key-value pairs to a dictionary:
# Dictionaries are a dynamic structure, like a list.
alien_0 = {"color": "green", "points": 5}
print(alien_0)

alien_0["x-position"] = 0
alien_0["y-position"] = 25
print(alien_0)

#These few lines added x-position-0 and y-position-25 to the dictionary!
